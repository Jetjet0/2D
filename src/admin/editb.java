    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
        String title = jTextField2.getText().trim();
        String author = jTextField4.getText().trim();
        String genre = jTextField5.getText().trim();
        String priceStr = jTextField3.getText().trim();
 
        if (title.isEmpty() || author.isEmpty() ||
            genre.isEmpty() || priceStr.isEmpty()) {
 
            JOptionPane.showMessageDialog(this, "Fill all fields!");
            return;
        }
 
        int price = Integer.parseInt(priceStr);
 
        byte[] imageBytes = existingImageBytes;
 
        // If new image selected → resize it
        if (selectedImageFile != null) {
 
            ImageIcon originalIcon =
                    new ImageIcon(selectedImageFile.getAbsolutePath());
            Image originalImage = originalIcon.getImage();
 
            BufferedImage resizedImage =
                    new BufferedImage(250, 375, BufferedImage.TYPE_INT_RGB);
 
            Graphics2D g2d = resizedImage.createGraphics();
            g2d.drawImage(originalImage, 0, 0, 250, 375, null);
            g2d.dispose();
 
            ByteArrayOutputStream baos = new ByteArrayOutputStream();
            ImageIO.write(resizedImage, "jpg", baos);
            imageBytes = baos.toByteArray();
        }
 
        String sql = "UPDATE tbl_books SET title=?, author=?, genre=?, price=?, picture=? WHERE bo_id=?";
 
        new cconfig().addRecord(sql,
                title, author, genre, price, imageBytes, editingBookId);
 
        JOptionPane.showMessageDialog(this, "Book updated successfully!");
 
        adbok booksPage = new adbok();
        booksPage.setVisible(true);
        this.dispose();
 
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Price must be a number!");
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Database error!");
    }

    }//GEN-LAST:event_jButton2ActionPerformed

