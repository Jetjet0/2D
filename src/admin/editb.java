    // ✅ Load selected book details
    private void loadBookDetails(int id) {
        try {
            Connection conn = cconfig.connectDB();
            PreparedStatement pst =
                    conn.prepareStatement("SELECT * FROM tbl_books WHERE bo_id=?");
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();
 
            if (rs.next()) {
                jTextField2.setText(rs.getString("title"));
                jTextField4.setText(rs.getString("author"));
                jTextField5.setText(rs.getString("genre"));
                jTextField3.setText(String.valueOf(rs.getInt("price")));
 
                existingImageBytes = rs.getBytes("picture");
 
                if (existingImageBytes != null) {
                    ImageIcon icon = new ImageIcon(existingImageBytes);
                    Image img = icon.getImage()
                            .getScaledInstance(250, 375, Image.SCALE_SMOOTH);
                    jButton1.setIcon(new ImageIcon(img));
                    jButton1.setText("");
                }
            }
 
            rs.close();
            pst.close();
            conn.close();
 
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    

